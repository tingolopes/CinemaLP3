    public void cadastrarFilme() {
        Filme filme = new Filme();

        List listaArt = new ArrayList();
        List listaGen = new ArrayList();

        String aux;
        String auxsplit[];

        aux = jTextFieldArtistas.getText();
        auxsplit = aux.split(",");

        for (int i = 0; i < auxsplit.length; i++) {
            listaArt.add(new Artista((auxsplit[i]).trim()));
        }

        aux = jTextFieldGeneros.getText();
        auxsplit = aux.split(",");
        for (int i = 0; i < auxsplit.length; i++) {
            listaGen.add(new Genero((auxsplit[i]).trim()));
        }

        filme = new Filme(jTextFieldNome.getText(), //Nome filme
                jTextFieldClassificacao.getText(), //Classificação
                jTextAreaSinopse.getText(), //Sinopse
                jSlider1.getValue(), //Duração
                listaArt, //Lista de artistas
                listaGen); //Lista de generos

        new DaoGenerico<Filme>().saveOrUpdate(filme);
    }

